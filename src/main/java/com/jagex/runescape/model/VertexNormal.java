package com.jagex.runescape.model;

public final class VertexNormal {

  public int x;
  public int y;
  public int z;
  public int c;

  public VertexNormal() {
  }

  public VertexNormal(VertexNormal var1) {
    this.c = var1.c;
    this.x = var1.x;
    this.y = var1.y;
    this.z = var1.z;
  }

}
